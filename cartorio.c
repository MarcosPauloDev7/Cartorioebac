int consulta() //Função responsavel pela consulta no sistema
{	
    setlocale(LC_ALL, "Portuguese"); //definindo a linguagem
    //início criação de variaveis/string
	char cpf[40];
	char conteudo[200];
	//final da criação de variaveis/string
	printf("Digite o cpf a ser consultado: "); //coletando informação do usuário
	scanf("%s",cpf); //refere-se a string
	
	FILE *file; //Cria o arquivo
	file = fopen(cpf,"r"); //Cria o arquivo o "r" significa ler
	
	if(file == NULL) 
	{
	printf("Não foi possivel abrir o arquivo, não localizado!.\n"); //Mensagem dadá quando não encontra o arquivo proucurado
    }
    
    while(fgets(conteudo, 200, file) != NULL)
    {
    	printf("\nEssas são as informações do usuário: ");
    	printf("%s", conteudo);
    	printf("\n\n");
	}
	
	system("pause");
}

